    private void carregarProduto() {
        try {
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/estoquecrypto", "root", "Sonho2013");
            String sql = "SELECT id, nome, preco_unitario, quantidade_estoque FROM produtos WHERE nome = ?";
            PreparedStatement stmt = conn.prepareStatement(sql);
            stmt.setString(1, nomeProduto);
            ResultSet rs = stmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
            model.setRowCount(0); // limpa a tabela antes

            while (rs.next()) {
                Object[] row = {
    rs.getInt("id"),
    rs.getString("nome"),
    rs.getDouble("preco_unitario"),
    rs.getInt("quantidade_estoque")
};

                model.addRow(row);
            }

            rs.close();
            stmt.close();
            conn.close();
        } catch (SQLException ex) {
            logger.log(java.util.logging.Level.SEVERE, null, ex);
            javax.swing.JOptionPane.showMessageDialog(this, "Erro ao carregar o produto.");
        }
    }
